package com.android.barcode;

public class Products_info {
    private String mph;
    private String wl;
    public Products_info(String mph,String wl){
        this.mph = mph;
        this.wl = wl;
    }
    public String getmph(){
        return mph;
    }
    public String getwl(){
        return wl;
    }
}
