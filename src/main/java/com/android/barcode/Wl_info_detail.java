package com.android.barcode;

public class Wl_info_detail {
    private String wl;
    private String mph;
    public Wl_info_detail(String wl,String mph ){
        this.wl = wl;
        this.mph = mph;
    }
    public String getWl(){
        return wl;

    }
    public String getMph(){
        return mph;
    }

}
